async function processInChunks(items, chunkSize, concurrency, handler) {
  let index = 0
  async function worker() {
    while (index < items.length) {
      const start = index
      index += chunkSize
      const chunk = items.slice(start, start + chunkSize)
      if (chunk.length === 0) continue
      await handler(chunk, start)
    }
  }
  const workers = Array.from(
    { length: Math.min(concurrency, Math.max(1, Math.ceil(items.length / chunkSize))) },
    () => worker()
  )
  await Promise.all(workers)
}


async function zeroPointsForStoreIds(storeIds) {
  if (storeIds.length === 0) {
    return { matchedCount: 0, modifiedCount: 0 }
  }

  let totalMatched = 0
  let totalModified = 0

  await processInChunks(
    storeIds,
    BATCH_SIZE,
    ENTITY_CONCURRENCY,
    async (storeChunk, start) => {
     const storeObjectIds = storeChunk.map(id => ObjectId(id))
      const filter = {
        store_id: { $in: storeObjectIds },
      }
      const targetedCount = await CustomerSegment.countDocuments(filter)

      const result = await CustomerSegment.updateMany(
        filter,
        {
          $set: {
            current_points: 0,
            total_redeemed_points: 0,
          },
        },
        { timestamps: false }
      )

      const matchedCount =
        result.matchedCount ?? result.n ?? result?.result?.n ?? targetedCount ?? 0
      const modifiedCount =
        result.modifiedCount ?? result.nModified ?? result?.result?.nModified ?? 0

      totalMatched += matchedCount
      totalModified += modifiedCount
      console.log(
        `  Chunk ${start + 1}-${start + storeChunk.length}: targeted=${targetedCount}, matched=${matchedCount}, modified=${modifiedCount}`
      )
    }
  )

  return { matchedCount: totalMatched, modifiedCount: totalModified }
}

async function run() {
  const uniqueIds = [...new Set(groupIds)].map(id => ObjectId(id))
  console.log(`Total unique IDs to process: ${uniqueIds.length}`)

  // Step 1: Classify IDs as stores or groups
  // Check which IDs exist as stores
  const foundStores = await Store.collection
    .find(
      { _id: { $in: uniqueIds } },
      { projection: { _id: 1, group: 1 } }
    )
    .toArray()

  const storeIdSet = new Set(foundStores.map(s => s._id.toString()))

  // IDs not found in stores → check if they are group IDs
  const potentialGroupIds = uniqueIds.filter(id => !storeIdSet.has(id.toString()))
  const foundGroups = potentialGroupIds.length > 0
    ? await Group.collection
        .find(
          { _id: { $in: potentialGroupIds } },
          { projection: { _id: 1 } }
        )
        .toArray()
    : []
  const groupIdSet = new Set(foundGroups.map(g => g._id.toString()))

  // Separate standalone stores vs stores with groups
  const standaloneStores = foundStores.filter(s => !s.group)
  const storesWithGroup = foundStores.filter(s => s.group)

  // Collect all unique group IDs (from explicit group IDs + stores that have a group)
  const allGroupIds = new Set([
    ...Array.from(groupIdSet),
    ...storesWithGroup.map(s => s.group.toString()),
  ])


  console.log(`\nClassification:`)
  console.log(`  Standalone stores: ${standaloneStores.length}`)
  console.log(`  Stores with group: ${storesWithGroup.length}`)
  console.log(`  Group IDs: ${groupIdSet.size}`)


  // Step 2: Resolve final store IDs
  const groupArray = Array.from(allGroupIds)
  const groupObjectIds = groupArray.map(id => ObjectId(id))

  // Fetch stores under groups in one query (faster than querying group-by-group)
  const groupStores = groupObjectIds.length
    ? await Store.collection
        .find(
          { group: { $in: groupObjectIds } },
          { projection: { _id: 1 } }
        )
        .toArray()
    : []

  const finalStoreIdSet = new Set([
    ...standaloneStores.map(s => s._id.toString()),
    ...groupStores.map(s => s._id.toString()),
  ])
  const finalStoreIds = Array.from(finalStoreIdSet).map(id => ObjectId(id))

  console.log(`\nStore resolution:`)
  console.log(`  Standalone stores considered: ${standaloneStores.length}`)
  console.log(`  Group stores considered: ${groupStores.length}`)
  console.log(`  Total unique stores to update: ${finalStoreIds.length}`)

  // Step 3: Zero points directly in customer_segments for all resolved stores
  console.log(`\n--- Zeroing customer_segments points (chunk=${BATCH_SIZE}, concurrency=${ENTITY_CONCURRENCY}) ---`)
  const { matchedCount, modifiedCount } = await zeroPointsForStoreIds(finalStoreIds)

  console.log(`\n=== DONE ===`)
  console.log(`Total customer_segments matched: ${matchedCount}`)
  console.log(`Total customer_segments modified: ${modifiedCount}`)
}

MongoConnection.connect(async function (error) {
  if (error) {
    console.error('MongoDB connection error:', error)
    process.exit(1)
  }

  try {
    await run()
    process.exit(0)
  } catch (err) {
    console.error('Script error:', err)
    process.exit(1)
  }
})
